package com.saicon.games.callcard.util;

/**
 * Enumeration of sort order types for list and query operations.
 * Defines how results should be sorted in various operations.
 *
 * @author Talos Maind Platform
 * @since 2025-12-21
 */
public enum SortOrderTypes {
    /**
     * Sort by ordering field in ascending order
     */
    BY_ORDERING_ASC,

    /**
     * Sort by ordering field in descending order
     */
    BY_ORDERING_DESC,

    /**
     * Sort by name field in ascending order
     */
    BY_NAME_ASC,

    /**
     * Sort by name field in descending order
     */
    BY_NAME_DESC
}
